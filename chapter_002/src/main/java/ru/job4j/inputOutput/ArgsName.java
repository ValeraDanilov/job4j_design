package ru.job4j.inputOutput;

import java.util.HashMap;
import java.util.Map;

public class ArgsName {

    private final Map<String, String> values = new HashMap<>();

    public String get(String key) {
        return values.get(key);
    }

    private void parse(String[] args) {
        if (args.length == 0) {
            throw new IllegalArgumentException("Arrays is null");
        }
        for (String element : args) {
            if (!element.startsWith("-") && element.contains("=")) {
                throw new IllegalArgumentException("The string is not correct");
            }
            String key = element.substring(element.indexOf('-') + 1, element.indexOf("="));
            String value = element.substring(element.indexOf('=') + 1);
            this.values.put(key, value);
        }
    }

    public static ArgsName of(String[] args) {
        ArgsName names = new ArgsName();
        names.parse(args);
        return names;
    }

    public static void main(String[] args) {
        ArgsName jvm = ArgsName.of(new String[]{"-Xmx=512", "-encoding=UTF-8"});
        System.out.println(jvm.get("Xmx"));

        ArgsName zip = ArgsName.of(new String[]{"-out=project.zip", "-encoding=UTF-8"});
        System.out.println(zip.get("out"));
    }
}
